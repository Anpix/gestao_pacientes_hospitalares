/**
 * Bean dos diagnosticos dos pacientes
 * @author Gabriel Lett Viviani <gm.lett@gmail.com>
 *
 */
public class Diagnostico {

	/** Enfermidade do paciente */
	private String enfermidade;

	/**
	 * @return the enfermidade
	 */
	public String getEnfermidade() {
		return enfermidade;
	}

	/**
	 * @param enfermidade the enfermidade to set
	 */
	public void setEnfermidade(String enfermidade) {
		this.enfermidade = enfermidade;
	}
}
